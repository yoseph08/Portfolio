// this makes sure nav bar doesnt overlap the page heading after navigating to a section from the nav bar

document.addEventListener("DOMContentLoaded", () => {
  const links = document.querySelectorAll('a[href^="#"]');

  links.forEach(link => {
    link.addEventListener("click", function (e) {
      e.preventDefault();

      const targetId = this.getAttribute("href").substring(1);
      const targetSection = document.getElementById(targetId);

      if (targetSection) {
    
        const navbarHeight = document.querySelector(".navigation").offsetHeight;
        const targetPosition = targetSection.offsetTop;

    
        window.scrollTo({
          top: targetPosition - navbarHeight -30, 
          behavior: "smooth",
        });
      }
    });
  });
});


